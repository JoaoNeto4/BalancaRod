    private OrigemFazenda retornaObjeto(){
        OrigemFazenda of = new OrigemFazenda();
        of.setOrigem(txtPesquisa.getText());
        return of;
    }
    
    public final void atualizarTabela(){
        try {
            listaOrigem = OrigemFazendaDao.pesquisar(retornaObjeto());
            DefaultTableModel modelo = (DefaultTableModel) tabelaTela.getModel();
            modelo.setNumRows(0);

            for (OrigemFazenda of : listaOrigem) {
                modelo.addRow(new Object[]{
                    of.getId(),
                    of.getParceiro().getFantasia(),
                    of.getOrigem()
                });
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }


